	<h2 style="margin-bottom: 15px;">ELDER CARE</h2>
	<div id="line" style="width: 150px; margin-top: 0px;"></div>
	
	<p>For elders, telmedx has reinvented the house call. The telmedx system offers significant benefits to improve the medical care of many senior citizens who have difficulty traveling to a doctor's office or medical facility. Using the telmedx system allows elderly patients to be connected with doctors easily and often right from the patient's home.<br />
	<br />
	We realize that many senior citizens may not own a cell phone, or they might not be comfortable relying on cell phone technology, but with telmedx, a caregiver, a family member, a neighbor or friend can connect with a doctor in the senior's home - or in any location, for that matter.<br />
	<br />By allowing doctors to interact with patients more frequently and more efficiently, a higher level of care can be delivered and elderly patients can avoid being displaced.</p>
